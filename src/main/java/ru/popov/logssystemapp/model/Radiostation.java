package ru.popov.logssystemapp.model;

import jakarta.persistence.*;
import lombok.Getter;
import lombok.Setter;

@Entity
@Table(name="radiostation")
@Setter
@Getter
public class Radiostation {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;
    @Column(name = "br_number", columnDefinition = "VARCHAR(100)")
    private String brNumber;
    @Column(name = "zav_number", columnDefinition = "VARCHAR(255)")
    private String zavNumber;
    @Column(name = "index_radio", columnDefinition = "VARCHAR(255)")
    private String indexRadio;
    @Column(name = "garnitura", columnDefinition = "VARCHAR(10)")
    private String garnitura;
    @Column(name = "chexol")
    private Boolean isChexol;



































































































































































































































































































































































}
